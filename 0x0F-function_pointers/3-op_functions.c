#include "3-calc.h"

/**
  *op_add - adds two integers
  *@a: the first integer
  *@b: the second integer
  *Return: always an integer of the addition
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  *op_sub - substracts two integers
  *@a: the first integer
  *@b: the second integer
  *Return: always an integer of the substraction
  */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
  *op_mul - multiplies two integers
  *@a: the first integer
  *@b: the second integer
  *Return: always an integer of the multiplication
  */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
  *op_div - divides two integers
  *@a: the first integer
  *@b: the second integer
  *Return: always quotient integer
  */

int op_div(int a, int b)
{
	return (a / b);
}
/**
  *op_mod - determines the modulus of the integers
  *@a: the first integer
  *@b: the second integer
  *Return: always modulus integer
  */

int op_mod(int a, int b)
{
	return (a % b);
}
